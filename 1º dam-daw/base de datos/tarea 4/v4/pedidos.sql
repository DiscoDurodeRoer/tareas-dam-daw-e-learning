CREATE TABLE CLIENTES (
	CODIGO NUMBER(8,0) PRIMARY KEY,
	NOMBRE VARCHAR2(30) NOT NULL,
	APELLIDOS VARCHAR2(30) NOT NULL,
	EDAD NUMBER(3,0) NOT NULL
);

CREATE TABLE PRODUCTOS (
	CODIGO NUMBER(8,0) PRIMARY KEY,
	NOMBRE VARCHAR2(30) NOT NULL,
	PRECIO NUMBER(8,2) NOT NULL
);

CREATE TABLE PEDIDOS (
	NUM NUMBER(8,0) PRIMARY KEY,
	FECHA DATE NOT NULL,
	GASTOS_ENVIO NUMBER(8,2) NULL,
	TOTAL NUMBER(8,2) NOT NULL,
	FECHA_PREVISTA DATE NOT NULL,
	CLIENTE NUMBER(8,0) NOT NULL,
	FOREIGN KEY (CLIENTE) REFERENCES CLIENTES(CODIGO)
);

CREATE TABLE LINEAS (
	NUM NUMBER(8,0),
	NUM_PEDIDO NUMBER(8,0),
	PRODUCTO NUMBER(8,0) NOT NULL,
	CANTIDAD NUMBER(8,0) NOT NULL,
	IMPORTE NUMBER(8,2) NOT NULL,
	PRIMARY KEY (NUM, NUM_PEDIDO),
	FOREIGN KEY (PRODUCTO) REFERENCES PRODUCTOS(CODIGO)
);




